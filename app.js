// --- Tab Controller ---
function switchTab(tabId) {
  appState.activeTab = tabId;

  // Update tabs buttons UI
  DOM.tabBtns.forEach(btn => {
    const isTarget = btn.id === 'tab' + tabId.charAt(0).toUpperCase() + tabId.slice(1);
    btn.classList.toggle('active', isTarget);
    btn.setAttribute('aria-selected', isTarget ? 'true' : 'false');
  });

  // Update tab panels display
  DOM.tabPanels.forEach(panel => {
    const isTarget = panel.id === 'panel' + tabId.charAt(0).toUpperCase() + tabId.slice(1);
    panel.classList.toggle('active', isTarget);
  });

  // If switched away from Podcast, pause it
  if (tabId !== 'podcast') {
    pausePodcast();
  }
}

// --- Custom Podcast Player Functions ---
function togglePodcastPlay() {
  if (appState.audioPlaying) {
    pausePodcast();
  } else {
    playPodcast();
  }
}

function playPodcast() {
  if (appState.audioTimer) clearInterval(appState.audioTimer);
  appState.audioPlaying = true;
  DOM.btnPlayPause.innerHTML = '<i class="fa-solid fa-pause"></i> השהה';
  DOM.audioWave.classList.add('playing');
  
  appState.audioTimer = setInterval(() => {
    appState.audioCurrentTime += appState.audioSpeed;
    if (appState.audioCurrentTime >= appState.audioDuration) {
      appState.audioCurrentTime = appState.audioDuration;
      stopPodcast();
      playSuccessSound();
    }
    updatePodcastProgressUI();
  }, 1000);
}

function pausePodcast() {
  appState.audioPlaying = false;
  DOM.btnPlayPause.innerHTML = '<i class="fa-solid fa-play"></i> נגן';
  DOM.audioWave.classList.remove('playing');
  if (appState.audioTimer) {
    clearInterval(appState.audioTimer);
    appState.audioTimer = null;
  }
}

function stopPodcast() {
  pausePodcast();
  appState.audioCurrentTime = 0;
  updatePodcastProgressUI();
}

function skipPodcastTime(seconds) {
  appState.audioCurrentTime = Math.min(Math.max(appState.audioCurrentTime + seconds, 0), appState.audioDuration);
  updatePodcastProgressUI();
  playClickSound();
}

function changePodcastSpeed() {
  appState.audioSpeed = parseFloat(DOM.selectSpeed.value);
  playClickSound();
}

function seekPodcast() {
  const percentage = parseFloat(DOM.audioProgress.value);
  appState.audioCurrentTime = (percentage / 100) * appState.audioDuration;
  updatePodcastProgressUI();
}

function updatePodcastProgressUI() {
  const currentMin = Math.floor(appState.audioCurrentTime / 60);
  const currentSec = Math.floor(appState.audioCurrentTime % 60);
  DOM.timeCurrent.textContent = `${currentMin.toString().padStart(2, '0')}:${currentSec.toString().padStart(2, '0')}`;
  
  const percentage = (appState.audioCurrentTime / appState.audioDuration) * 100;
  DOM.audioProgress.value = percentage;
}

// --- Presentation Slider Functions ---
function renderSlide() {
  const catData = CONTENT_DATABASE[appState.activeCategory];
  if (!catData || !catData.presentation) return;
  
  const slide = catData.presentation[appState.slideIndex];
  const totalSlides = catData.presentation.length;

  // Build bullets HTML
  let bulletsHtml = "";
  if (slide.bullets && slide.bullets.length > 0) {
    bulletsHtml = `<ul class="slide-bullet-list">`;
    slide.bullets.forEach(bullet => {
      bulletsHtml += `<li>${bullet}</li>`;
    });
    bulletsHtml += `</ul>`;
  }

  // Render Slide Content
  DOM.activeSlideContent.innerHTML = `
    <div class="slide-content-layout" lang="${catData.isArabic ? 'ar' : 'he'}" style="text-align: right;">
      <h4 class="slide-title-header">${slide.title}</h4>
      <p class="slide-body-text">${slide.text}</p>
      ${bulletsHtml}
      <div class="slide-graphic-box">
        ${slide.graphic}
      </div>
    </div>
  `;

  // Update Counters & Dots
  if (catData.isArabic) {
    DOM.slideCounterDisplay.textContent = `شريحة ${appState.slideIndex + 1} من ${totalSlides}`;
  } else {
    DOM.slideCounterDisplay.textContent = `שקופית ${appState.slideIndex + 1} מתוך ${totalSlides}`;
  }
  
  // Dots UI
  DOM.slideDotsContainer.innerHTML = "";
  for (let i = 0; i < totalSlides; i++) {
    const dot = document.createElement('button');
    dot.className = `slide-dot ${i === appState.slideIndex ? 'active' : ''}`;
    dot.setAttribute('aria-label', `עבור לשקופית ${i + 1}`);
    dot.addEventListener('click', () => {
      appState.slideIndex = i;
      renderSlide();
      playClickSound();
    });
    DOM.slideDotsContainer.appendChild(dot);
  }
}

function changeSlide(direction) {
  const catData = CONTENT_DATABASE[appState.activeCategory];
  if (!catData || !catData.presentation) return;
  
  const totalSlides = catData.presentation.length;
  // Account for RTL direction (Prev and Next reversed visually)
  appState.slideIndex = (appState.slideIndex + direction + totalSlides) % totalSlides;
  renderSlide();
  playClickSound();
}

function togglePresentationAutoplay() {
  if (appState.isAutoplay) {
    stopPresentationAutoplay();
  } else {
    startPresentationAutoplay();
  }
  playClickSound();
}

